    public void loadDataCombo() {
        modelCombo.removeAllElements();//xóa hết dữ liệu cũ trên combo
        //gọi phương thức getComboBox cửa DiemService để lấy dữ liệu
        ArrayList<IDStudent> lst = diemService.getComboBox();
        //Duyệt lst => lấy dữ liệu đổ bào combo/modelCombo
        for (IDStudent n : lst) {
            modelCombo.addElement(n);
        }

    }

    public void loadTable() {
        model.setColumnCount(0);
        model.addColumn("STT");
        model.addColumn("IDSTUDENT");
        model.addColumn("Mã SV");
        model.addColumn("Họ tên");
        model.addColumn("Tiếng anh");
        model.addColumn("Tin học");
        model.addColumn("GDTC");
        model.addColumn("Điểm TB");

        list = diemService.getAllDiem();
        model.setRowCount(0);
        for (Diem d : list) {
            Object[] banGhi = new Object[]{
                d.getID(),
                d.getIDStudent(),
                d.getMaSV(),
                d.getHoTen(),
                d.getTiengAnh(),
                d.getTinHoc(),
                d.getGDTC(),
                d.getDiemTB()
            };
            model.addRow(banGhi);
        }
    }

