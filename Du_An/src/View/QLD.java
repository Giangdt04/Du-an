    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (vaiTro.equalsIgnoreCase("SINHVIEN")) {
            JOptionPane.showMessageDialog(this, "Bạn không đủ quyền hạn để thực hiện chức năng này");
        } else {
            int choose = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa?", "Xóa", JOptionPane.YES_NO_OPTION);
            if (choose == JOptionPane.NO_OPTION) {
                return;
            }
            try {
                if (txtIDStudent.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Mã không được để trống");
                    return;
                }

                String ID = txtIDStudent.getText();
                int deleteDiem = diemService.deleteDiem(ID);
                if (deleteDiem > 0) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    loadTable();

                } else {
                    JOptionPane.showMessageDialog(this, "Xoa thất bại");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

