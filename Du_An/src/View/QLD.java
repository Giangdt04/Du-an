    public Diem readForm() {
        if (txtMaSV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập mã");
            return null;
        }
        Diem x = new Diem();
        IDStudent id = (IDStudent) modelCombo.getSelectedItem();
        
        x.setIDStudent(id);
        x.setMaSV(txtMaSV.getText());
        x.setHoTen(txtHoTen.getText());
        x.setTiengAnh(Integer.valueOf(txtTiengAnh.getText()));
        x.setTinHoc(Integer.valueOf(txtTinHoc.getText()));
        x.setGDTC(Integer.valueOf(txtGDTC.getText()));
        return x;
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (vaiTro.equalsIgnoreCase("SINHVIEN")) {
            JOptionPane.showMessageDialog(this, "Bạn không đủ quyền hạn để thực hiện chức năng này");
        } else {
            try {
                Diem x = readForm();
                
                Integer save = diemService.addDiem(x);
                if (save > 0) {
                    JOptionPane.showMessageDialog(this, "thêm thành công");
                    loadTable();
                } else {
                    JOptionPane.showMessageDialog(this, "thêm không thành công");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Không thể thêm được");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

