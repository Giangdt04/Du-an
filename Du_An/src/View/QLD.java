    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        boolean gt;
        if (rbtNu.isSelected()) {
            gt = false;
        } else {
            gt = true;
        }
        String ma = txtSearchMa.getText();
        String hoTen = txtSearchTen.getText();
        ArrayList<Diem> search = diemService.searchSinhVien(ma, hoTen, gt);
        model.setRowCount(0);
        for (Diem d : search) {
            Object[] banGhi = new Object[]{
                d.getID(),
                d.getIDStudent(),
                d.getMaSV(),
                d.getHoTen(),
                d.getTiengAnh(),
                d.getTinHoc(),
                d.getGDTC(),
                d.getDiemTB()
            };
            model.addRow(banGhi);
        }

    }//GEN-LAST:event_btnSearchActionPerformed

